    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $currentCart = Cart::where('product_id', $request->productId)->where('user_id', auth()->id())->first();

        $data = [
            'product_id' => $request->productId,
            'user_id' => auth()->id(),
            'quantity' => ($currentCart)? $currentCart->quantity+$request->quantity : $request->quantity
        ];

        Cart::updateOrCreate(['product_id' => $request->productId, 'user_id' => auth()->id() ], $data);

        return redirect(route('cart.index'))->with('success', 'Successfully Added to Cart!');
    }

